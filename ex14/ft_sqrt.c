int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	while (i < nb && (i * i) <= 2147483647)
	{
		if (i * i == nb)
			return (i);
		else if (i * i < nb)
		{
			if ((i + 1) * (i + 1) > nb)
				return (0);
			else
				i++;
		}
	}
	return (0);
}
